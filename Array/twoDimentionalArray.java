package Array;
//calculate the length of each row
public class twoDimentionalArray {
	public static void main(String args[]) {
		int [][] number = {{4,5,6,8},
				{5,9,7},
				{7},};
		System.out.println("Length of 1st row " + number[0].length);
		System.out.println("Length of 2nd row " + number [1].length);
		System.out.println("Length of 3rd row " + number [2].length);
		
	}

}
