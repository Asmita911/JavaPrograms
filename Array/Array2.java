package Array;
//array using loop
public class Array2 {
	public static void main(String args[]){
		int[] age = {45,63, 12, 23};
		
		System.out.println("Enter the Age Elements :");
		
		for(int i = 0; i<age.length; i++) {
		System.out.println(age[i]);
		
	}

}
}
